    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            Valores();
            if (!TServerName.getText().equals("") && !TLogin.getText().equals("") && !TPassword.getText().equals("") ){
            QuerySQLServer SQL = new QuerySQLServer();
            ArrayResult = SQL.Nombre_Base();
            CBDSQL.removeAllItems();
            for (String[] elemento : ArrayResult) {
                CBDSQL.addItem(elemento[0]);
            }
            }else{
                JOptionPane.showMessageDialog(null, "Favor de capturar parametros de conexion!");
                // JOptionPane.showMessageDialog(null, "Hola Anita esto lo acabo de actualizar!");
            }

        } catch (SQLException ex) {
            Logger.getLogger(IntegraTMP.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Error de Conexion" + ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

