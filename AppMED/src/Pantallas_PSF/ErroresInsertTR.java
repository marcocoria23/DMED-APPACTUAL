        public void limpiarTabla() {
            DefaultTableModel TablaError = (DefaultTableModel) jTable1.getModel();
            while (TablaError.getRowCount() > 0) {
                TablaError.removeRow(0);
            }
        }

        public void MuestraError() {
            try {
                IntegraPSF_TR In=new IntegraPSF_TR();
                jTextArea1.setVisible(false);
                jProgressBar1.setVisible(false);
                ExportaEi.setVisible(false);
                String Texto = "";
                String regTMPSQL = "",regTRSQL="", regTRSQLNI = "", RegValidacion = "";
                ArrayList<String[]> ArrayErroresIns;
                Querys Q = new Querys();
                String[] ArrayTablasTMP = new String[26];
                ArrayTablasTMP[0] = "TMP_GF_CONTRATACIONES_PUBLICAS_GEN";
                ArrayTablasTMP[1] = "TMP_GF_POBLACION_ATEND_MPIO_GEN";
                ArrayTablasTMP[2] = "TMP_GF_PROG_SOCIALES_GEN";
                String[] ArrayTablasTR = new String[26];
                ArrayTablasTR[0] = "TR_GF_CONTRATACIONES_PUBLICAS_"+In.Año;
                ArrayTablasTR[1] = "TR_GF_POBLACION_ATEND_MPIO_"+In.Año;
                ArrayTablasTR[2] = "TR_GF_PROG_SOCIALES_"+In.Año;
                ArrayErroresIns=Q.TotalErrores_insert_TR(In.Año);
                jProgressBar1.setVisible(true);
                int Contador = 30;
                for (int i = 0; i < 3; i++) {
                    regTMPSQL = Q.Total_Reg_TMP(ArrayTablasTMP[i]);
                    regTRSQL=Q.Total_Reg_TMP(ArrayTablasTR[i]);
                    regTRSQLNI = Q.Total_Reg_NITMP(ArrayTablasTR[i]);
                    Texto = Texto + "" + ArrayTablasTR[i] + ": \n"
                    + "Reg_TMP:" + regTMPSQL + "  Reg_TR:" + regTRSQL + "  Reg_TRNI:" + regTRSQLNI + "\n \n";
                    jProgressBar1.setValue(Contador);
                    Contador = Contador + 35;
                }   jTextArea1.setText(Texto);
                jTextArea1.setVisible(true);
                if (ArrayErroresIns.size() > 0) {
                    String[] errorarray;
                    String part0 = "", part1 = "", part2 = "", part3 = "";
                    DefaultTableModel TablaError = (DefaultTableModel) jTable1.getModel();
                    Object[] fila = new Object[5];
                    for (int j = 0; j < ArrayErroresIns.size(); j++) {
                        String Datos = Arrays.toString(ArrayErroresIns.get(j)).replace("[", "").replace("]", "");
                        errorarray = Datos.split(",");
                        part0 = errorarray[0].trim();
                        part1 = errorarray[1].trim();
                        part2 = errorarray[2].trim();
                        fila[0] = part0;
                        fila[1] = part1;
                        fila[2] = part2;
                        TablaError.addRow(fila);
                        Class<?> col_class = jTable1.getColumnClass(0);
                        jTable1.setDefaultEditor(col_class, null);
                    }
                    ExportaEi.setVisible(true);
                } else {

                    JOptionPane.showMessageDialog(null, "No se encontraron registros con Error de insert", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (SQLException ex) {
                Logger.getLogger(ErroresInsertTMP.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jButton1ActionPerformed

