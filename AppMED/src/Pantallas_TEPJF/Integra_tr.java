    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        new Thread(() -> {
            insert_TR_EXPEDIENTESTEPJF InsExpe = new insert_TR_EXPEDIENTESTEPJF();
            insert_TR_ACTORESTEPJF InsAct = new insert_TR_ACTORESTEPJF();
            insert_TR_RESPONSABLESTEPJF InsRes = new insert_TR_RESPONSABLESTEPJF();
            insert_TR_RESOLUCIONESTEPJF InsResol = new insert_TR_RESOLUCIONESTEPJF();
            EliminaTR Elim = new EliminaTR();
            LProc.setVisible(true);
            Progress.setVisible(true);

            try {
                jButton2.setEnabled(false);
                jComboBox1.setEnabled(false);
                jToggleButton1.setEnabled(false);
                Elim.EliminaTR();
                LProc.setText("Ins..TR_TEPJF_EXPEDIENTE");
                Progress.setValue(25);
                InsExpe.InsertTR_Expedientes();
                LProc.setText("Ins..TR_TEPJF_ACTORES");
                Progress.setValue(50);
                InsAct.InsertTR_Actores();
                Progress.setValue(75);
                LProc.setText("Ins..TR_TEPJF_RESPONSABLES");
                InsRes.InsertTR_Responsables();
                Progress.setValue(100);
                LProc.setText("Ins..TR_TEPJF_RESOLUCIONES");
                InsResol.InsertTR_Resoluciones();
                JOptionPane.showMessageDialog(null, "Se ha insertado correctamente favor de verificar errores de insert");
                LProc.setVisible(false);
                Progress.setVisible(false);
                jButton2.setEnabled(true);
                jComboBox1.setEnabled(true);
                jToggleButton1.setEnabled(true);
            } catch (SQLException ex) {
                System.out.println("Error al migrar de TEMP a TR " + ex);
            }

        }).start();


    }//GEN-LAST:event_jToggleButton1ActionPerformed

