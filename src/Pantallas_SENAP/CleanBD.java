    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        new Thread(() -> {
            String Estatus = "";
            QueryOracle Q = new QueryOracle();
            Valores();
            EliminaTMPSENAP ELTMP = new EliminaTMPSENAP();
            EliminaTRSENAP ELTR = new EliminaTRSENAP();
            try {
                if (CPeriodo.getItemCount() > 0) {
                    Estatus = Q.Estatus_Control(Entidad, Periodo);
                    if (!Estatus.equals("Liberado")) {

                        int respuesta = JOptionPane.showConfirmDialog(null, "¿Estas seguro de Limpiar Base de Datos TR-TMP  de la Entidad:" + Entidad + " con periodo:" + Periodo + "?", "Pregunta", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                        if (respuesta == JOptionPane.YES_OPTION) {
                            BarraElimina Elim = new BarraElimina();
                            Elim.setVisible(true);
                            jButton1.setEnabled(false);
                            CEntidad.setEnabled(false);
                            CPeriodo.setEnabled(false);
                            if (RTMP.isSelected()) {
                                Q.Limpia_tr_senap_Control(Entidad, Periodo);
                                ELTMP.EliminaTMPSENAP(Entidad, Periodo);
                            }
                            if (RTR.isSelected()) {
                                Q.Limpia_tr_senap_Control(Entidad, Periodo);
                                ELTR.EliminaTRSENAP(Entidad, Periodo);
                            }
                            if (RTMPTR.isSelected()) {
                                Q.Limpia_tr_senap_Control(Entidad, Periodo);
                                ELTMP.EliminaTMPSENAP(Entidad, Periodo);
                                ELTR.EliminaTRSENAP(Entidad, Periodo);
                            }
                            if (ELTMP.Error = false) {
                                if (ELTR.Error = false) {
                                    JOptionPane.showMessageDialog(null, "La base de datos se ha limpiado con exito!!");
                                }
                            }

                            Elim.setVisible(false);
                            CEntidad.setEnabled(true);
                            CPeriodo.setEnabled(true);
                            jButton1.setEnabled(true);
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "La informacion Entidad: " + Entidad + " y Periodo:" + Periodo + " no puede ser eliminada ya que su estatus=Liberado");
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Favor de seleccionar Periodo");
                }

            } catch (SQLException ex) {
                Logger.getLogger(IntegraTMP.class.getName()).log(Level.SEVERE, null, ex);
            }

        }).start();


    }//GEN-LAST:event_jButton1ActionPerformed

